    private int centesimascegundos =0 , segundos= 0 , minutos =0 , horas =0 ; 
    private ActionListener acciones = new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            centesimascegundos++;  
            
            if (centesimascegundos==100){ // 100 centesimas de segundo es un 1sg
                segundos++; 
                centesimascegundos=0; // y regresa a 0 las centesimas de segundos
            }
            if (segundos==60){ // ah pasado un minuto
                minutos++; 
                segundos=0; 
            }
            if (minutos ==60){
                horas++; 
                minutos=0;
            }
            ActualizarEtiquetaTiempo();
        }
    }; 
    
    private void ActualizarEtiquetaTiempo (){
        String texto = (horas<=9?"0":"")+horas+":"+(minutos<=9?"0":"")+minutos+":"+(segundos<=9?"0":"")+segundos +":"+(centesimascegundos<=9?"0":"")+centesimascegundos;
        // si el horas llega a nueve quiero que pongas un 0 adelante  
        etiquetatime.setText(texto);
    }
    
    
