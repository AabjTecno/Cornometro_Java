    private void BStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BStopActionPerformed
      // si el tiempo esta corriendo 
        if (tiempo.isRunning()){
            tiempo.stop(); 
            Bcomenzar.setEnabled(true);
        }
        Bcomenzar.setText("Comenzar"); 
        Bpausa.setEnabled(false); 
        BStop.setEnabled(false);  
        
        
        horas =0 ;  
        minutos=0;  
        segundos=0 ;  
        centesimascegundos=0 ;  
        
        ActualizarEtiquetaTiempo();
    }//GEN-LAST:event_BStopActionPerformed

    private void BpausaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BpausaActionPerformed
        tiempo.stop(); 
        // habilitar iniciar 
        Bcomenzar.setEnabled(true);  
        Bpausa.setEnabled(false);
    }//GEN-LAST:event_BpausaActionPerformed

    private void BcomenzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BcomenzarActionPerformed
      // va comenzar que corra el tiempo
        tiempo.start();
        // quiero que no permita volver presionar el boton iniciar
        Bcomenzar.setEnabled(false);
        Bcomenzar.setText("Reanudar"); 
        // ademas quiero que los otros botones esten habilitados 
        Bpausa.setEnabled(true); 
        BStop.setEnabled(true);
        
    }//GEN-LAST:event_BcomenzarActionPerformed

